#Finding character in a string by subscripting, number in square\n
#determines what character pulls out. ALWAYS STARTS AT ZERO
print("Hello"[-3])
#Can also use negatives. -3 will mean character #3

#String
print("123+130")

#Integer - Whole Numbers
print(123+130)

#Larger Integers - commas for separate numbers, underscores for larger ones
print(124234745)
print(124_234_745)
print(313,523,754)

#FLoat - numbers with decimals
print(23.313)

#Boolean - True or False for either A or B
print(True)
print(False)
